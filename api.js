var t=0
var x=3;

function _init() {
    cls(); // clear screen
    rect(0,0,127,127,1);

    // play music from pattern 02
    // (loop back flag set in pat 13)
    // MUSIC NOT SUPPORTED
    // music(0)

    // play sound 0 on channel 3
    // SOUNDS NOT SUPPORTED
    // sfx(0, 3)

    // draw palette
    rectfill(1, 1, 7, 7, 5);
    _.times(16, function (i) {
        print(i, x, 2, i);
        x = x + 6 + flr(i/10) * 4;
    });

    // gfx shapes
    camera(-10, 0); // shift draws
    rectfill(10, 10, 18, 20, 8);
    rect(20, 10, 28, 20, 9);
    circfill(35, 15, 5, 10);
    circ(45, 15, 5, 11);
    line(52, 10, 58, 20, 12);

    pset(60, 10, 13);
    pset(62, 10, pget(60, 10)+1);
    clip(72, 10, 8, 10);
    rectfill(0, 0, 127, 127, 6);
    clip();


    // sprites
    rectfill(60, 24, 85, 33, 15);
    spr(1, 10, 25);
    spr(1, 20, 25, 1, 1, true);
    pal(15, 12) // remap 15 to 12
    sspr(8, 0, 8, 8,  30, 25, 24, 8);
    pal(15, 0) // draw solid black;
    pal(1, 8);
    sspr(8, 0, 8, 8,  60, 25, 24, 8);
    pal(); // reset palette mapping

    // map
    mset(3, 3, mget(1, 3)+2);
    map(0, 3, 10, 35, 4, 3, 1);
    fset(6, 1, true) // pink flower
    // only cels with flag 2 set
    map(0, 3, 50, 35, 4, 3, 2);


    // set cursor position and color
    cursor(0, 90);
    color(7);

    // math
    cursor(0, 70);
    x = cosp8(0.125) + mid(1, 2, 3);
    x = (x%1) + abs(-1);
    print("x: " + x);


    // collections and strings
    // HANDLED BY JAVASCRIPT
    var a = []
    a.push(11);
    a.push(12);
    a.push(13);
    a.push(14);
    a = _.filter(a, function (x) { return x !== 13})
    var str = 'a: ';
    _.each(a, function (ax) {
        str += ax + ' ';
    });
    print(str);

    // foreach and anon functions
    // HANDLED BY JAVASCRIPT
    var total=0;
    _.each(a, function (i) {
        total += i
    });
    print("total: " + total);


    // callbacks and input
    // nano-8 automatically loops
    // and calls _update() and
    // _draw() once per frame if
    // they exist

}

function _update() {
    t = t + 1;
}

function _draw() {
    // show current value of t
    rectfill(0, 90, 30, 96, 5);
    print("t: " + t, 1, 91 , 7);

    // show state of buttons

    print("buttons: ", 1, 101, 7);

    _.times(2, function (p) {
        _.times(6, function (i) {
            var col=13;
            if (btn(i, p)) col=7;
            rectfill(40 + i * 10, 101 + p * 10,
                     48 + i * 10, 105 + p * 10, col);

        });
    });

}

